class Date {
    constructor(day,month,year) {
        this.day = day;
        this.month = month;
        this.year = year ;

    }
    getDay() {
        return this.day
    }
    getMonth(){
        return this.month
    }
    getYear() {
        return this.year
    }
    setDay(day){
        return this.day = day;
    }
    setMonth(month){
        return this.month = month;
    }
    setYear(year){
        return this.year = year;
    }

}